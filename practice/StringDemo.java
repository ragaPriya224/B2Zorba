package practice;

public class StringDemo {

	public static void main(String[] args) {

		String name =" Good morning ";
		System.out.println(name);
		System.out.println(name.length());
		System.out.println(name.toLowerCase());
		System.out.println(name.toUpperCase());
		//		System.out.println(name + "team");
		System.out.println(name.concat("team"));//append
		System.out.println(name.charAt(2));
		System.out.println(name.isEmpty());
		System.out.println(name.substring(7)); //a part of
		System.out.println(name.indexOf('d'));
		System.out.println(name.indexOf('o'));
		System.out.println(name.lastIndexOf('o'));
		System.out.println(name);
		System.out.println(name.trim());
		//		System.out.println(name.);




	}

}
