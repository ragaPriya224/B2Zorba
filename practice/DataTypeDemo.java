package practice;

public class DataTypeDemo {

	public static void main(String[] args) {
//		System.out.println("**********");
//		parleG choclate = 10;
//		datatype
		byte choclate = 10;
//		byte -> datatype
//		choclate -> reference name/variable name
//		= -> assigning
//		10 -> value
		int pizza = 20;
		System.out.println(pizza);
		
		short burger = 150;
		long pen = 25L;
		float coffee = 23.5f; //small or caps f
		double tea = 46.887987987f;
		
		boolean value = true;
		boolean check = false;
		System.out.println(check);
		char pencil = 'z';
		System.out.println(pencil);
		char Pencil = 'k'; // quotes mandatory
		System.out.println(Pencil);
//		case sensitive
		
	int cup = 20;
	System.out.println(cup);
//	int keys = 20; declaration & initialization 
	int keys; //declaration
	keys = 20; //late intialization 
	
	System.out.println(keys);
		
	int a, b, c;            // Declares three ints, a, b, and c.
	int a = 10, b = 10;     // Example of initialization
	byte B = 22;            // initializes a byte type variable B.
	double pi = 3.14159;    // declares and assigns a value of PI.
	char a = 'a';           // the char variable a is initialized with value a

	}

}
